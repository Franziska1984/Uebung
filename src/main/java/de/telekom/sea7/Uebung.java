package de.telekom.sea7;

public class Uebung {

	public static void main(String[] args) {
			
		System.out.println((double)2); 				// zeigt mit Kommastelle an
		
		System.out.println((int)7.8); 			    // zeigt ohne Kommastelle an
		
		boolean istHeuteMontag = true;  			// Boolean Variable anlegen
		System.out.print("Ist heute Montag " + istHeuteMontag);
		
		int x = 12;  							   // if else Anweisung
		if (x < 10) {
			System.out.println(", x ist kleiner als 10");
		}
		else {
			System.out.println(", x ist größer als 10");
			
		}
		
	}

}


